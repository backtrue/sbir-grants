async def MCP_get_section_generation_prompt(section_id: str) -> str:
    """
    MCP async wrapper: 返回指定章節的生成提示詞與指引
    供 Claude Desktop 在撰寫每個章節前取得結構化提示
    """
    if section_id not in PROMPT_DATABASE:
        available = ", ".join(PROMPT_DATABASE.keys())
        return f"❌ 找不到章節：{section_id}\n\n可用章節：{available}"

    section = PROMPT_DATABASE[section_id]

    key_elements_str = "\n".join(f"  - {e}" for e in section["key_elements"])  # type: ignore
    checklist_str = "\n".join(f"  - [ ] {c}" for c in section["checklist"])  # type: ignore

    result = f"""## 📝 {section['title']} — 撰寫指引

### 專家人格設定
{section['expert_persona']}

### 必須涵蓋的要素（建議字數：約 {section['word_count']} 字）
{key_elements_str}

### 撰寫前自我檢核清單
{checklist_str}

---
💡 **提示**：請先使用此指引框架，再結合您專案的具體資料，由 Claude 協助您生成符合 SBIR 審查標準的完整內容。
"""
    return result
